#include "utils.h"

t_rgb		int_to_rgb(int col)
{
	t_rgb rgb;

	rgb.b = col % 256;
	rgb.g = ((col - rgb.b) / 256) % 256;
	rgb.r = ((col - rgb.b) / (256 * 256)) - (rgb.g / 256);
	return (rgb);
}
